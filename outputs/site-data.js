window.siteDataStore = {
  key: "ecommerce-site-data-v1",
  clone(value) {
    return JSON.parse(JSON.stringify(value));
  },
  isServerMode() {
    return location.protocol === "http:" || location.protocol === "https:";
  },
  load() {
    if (this.isServerMode()) return this.clone(window.DEFAULT_SITE_DATA);
    const saved = localStorage.getItem(this.key);
    if (!saved) return this.clone(window.DEFAULT_SITE_DATA);
    try {
      return Object.assign(this.clone(window.DEFAULT_SITE_DATA), JSON.parse(saved));
    } catch (error) {
      return this.clone(window.DEFAULT_SITE_DATA);
    }
  },
  save(data) {
    try {
      localStorage.setItem(this.key, JSON.stringify(data));
    } catch (error) {}
    if (!this.isServerMode()) return;
    fetch("/api/site-data", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(data)
    }).then((response) => {
      if (!response.ok) throw new Error("save failed");
    }).catch(() => {
      alert("保存到服务器失败，请确认网站是用 start-website.bat 启动的。");
    });
  },
  reset() {
    localStorage.removeItem(this.key);
  }
};
